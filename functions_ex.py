# Írjátok meg a signum függvényt! Egy paramétere van, ami egy szám.
# Ha a szám kisebb, mint 0, akkor -1-et vissza
# Ha 0, akkor 0-át ad vissza
# Ha nagyobb, mint 0, akkor 1-et ad vissza
def signum(n):
    if n < 0:
        return -1
    elif n == 0:
        return 0
    else:
        return 1

print(signum(30))

